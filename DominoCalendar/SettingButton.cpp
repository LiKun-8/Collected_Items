#include "SettingButton.h"
#include <QToolButton>
#include <QSize>

//.._SettingButton类实现

    /* 构造函数实现 */
    _DIYButton::_DIYButton(QWidget *_Parent)
        : QToolButton(_Parent)
        {
        setSizePolicy(QSizePolicy::Preferred, QSizePolicy::Preferred);
        setToolButtonStyle(Qt::ToolButtonIconOnly);
        setAutoRaise(true);
        }

    /* sizeHint()函数重写 */
    QSize _DIYButton::sizeHint() const
        {
        QSize _NewSize = QToolButton::sizeHint();
        _NewSize.rheight() += 6;
//        _NewSize.rwidth() -= 5;
        return _NewSize;
        }

